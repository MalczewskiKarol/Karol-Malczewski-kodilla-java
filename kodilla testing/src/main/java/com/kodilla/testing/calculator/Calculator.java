package com.kodilla.testing.calculator;

public class Calculator {
    private int add;
    private int substract;

    public Calculator() {
        this.add = add;
        this.substract = substract;
    }

    public int add(int a, int b) {
        return a + b;
    }

    public int substract(int a, int b) {
        return a - b;
    }
}